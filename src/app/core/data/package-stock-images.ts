const imageGroups = PACKAGE_STOCK_IMAGES.reduce((acc, item) => {
  const key = normalizePath(item.path);
  if (!key) {
    return acc;
  }

  if (!acc.has(key)) {
    acc.set(key, []);
  }

  acc.get(key)!.push(item);
  return acc;
}, new Map<string, PackageStockImage[]>());

const selectedImageByPath = new Map<string, string>();

export function getStockImageByPath(path: string): string | null {
  const normalized = normalizePath(path);
  if (!normalized) {
    return null;
  }

  // Para corporativos y otros solo se permite match exacto por paquete,
  // evitando caer a imágenes genéricas de carpetas padre.
  if (
    (normalized.startsWith('servicios/corporativos/') ||
      normalized.startsWith('servicios/otros/')) &&
    !imageGroups.has(normalized)
  ) {
    return null;
  }

  if (selectedImageByPath.has(normalized)) {
    return selectedImageByPath.get(normalized) ?? null;
  }

  let current = normalized;
  while (current) {
    const group = imageGroups.get(current);
    if (group?.length) {
      const picked = group[Math.floor(Math.random() * group.length)]!;
      selectedImageByPath.set(normalized, picked.imageUrl);
      return picked.imageUrl;
    }

    const nextSlash = current.lastIndexOf('/');
    if (nextSlash <= 0) {
      break;
    }
    current = current.slice(0, nextSlash);
  }

  return null;
}

function normalizePath(path: string | null | undefined): string {
  return String(path ?? '')
    .trim()
    .toLowerCase()
    .replace(/\\+/g, '/')
    .replace(/\/{2,}/g, '/')
    .replace(/^\/+|\/+$/g, '');
}
